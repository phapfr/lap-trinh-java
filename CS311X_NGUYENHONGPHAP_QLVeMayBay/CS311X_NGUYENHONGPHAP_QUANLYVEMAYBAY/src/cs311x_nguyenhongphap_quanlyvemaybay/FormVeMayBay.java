    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String ma, ngay, dd;
        double sl, dg;
        ma=txtMaSanBay.getText();
        ngay=txtNgayBay.getText();
        sl=Double.parseDouble(txtDonGia.getText());
        dg=Double.parseDouble(txtSoLuong.getText());
        dd=txtDiemDen.getText().trim();
        VeMayBay x;
        if (dd.equalsIgnoreCase("A")||dd.equalsIgnoreCase("My"))
            x=new XuatNgoai(dd, ma, ngay, dg, sl);
            else x=new NoiDia(dd, ma, ngay, dg, sl);
        if(a.themMoi(x)==true)
            hienThiThongTinTable(a.getA());
        else JOptionPane.showMessageDialog(null, "Khong them vi da co ma "+ma+" nay trong DS");
        
    }//GEN-LAST:event_btnThemActionPerformed

