    void xoaTable(){
        while (dtm.getRowCount()>0)
            dtm.removeRow(0);
    }
    void hienThiThongTinTable(ArrayList<VeMayBay>a){
        xoaTable();
        for (VeMayBay x : a)
            if (x instanceof NoiDia)
                dtm.addRow(new Object[]{x.getMaSanBay(),x.getNgayBay(),x.getDonGia(),x.getSoLuong(),((NoiDia) x).getTinh(),((NoiDia) x).tinhThanhTien()});
            else dtm.addRow(new Object[]{x.getMaSanBay(),x.getNgayBay(),x.getDonGia(),x.getSoLuong(),((XuatNgoai) x).getChauLuc(),((XuatNgoai) x).tinhThanhTien()});
    }

