    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        
        String ma, ngay, nsx;
        double sl,dg;
        ma=txtMaHang.getText();
        ngay=txtNgayNhap.getText();
        sl=Double.parseDouble(txtSoLuong.getText());
        dg=Double.parseDouble(txtDonGia.getText());
        nsx=txtSanXuat.getText().trim();
        HangHoa x;
        if(nsx.equalsIgnoreCase("TN")||nsx.equalsIgnoreCase("NK"))
            x= new HangDienMay(nsx, ma, ngay, sl, dg);
        else x= new HangThuCong(nsx, ma, ngay, sl, dg);
        if(a.themMoi(x)==true)
            hienThiThongTinTable(a.getA());
        else JOptionPane.showMessageDialog(null, "Khong them vi da co ma "+ma+" trong danh sach");
        
    }//GEN-LAST:event_btnThemActionPerformed

