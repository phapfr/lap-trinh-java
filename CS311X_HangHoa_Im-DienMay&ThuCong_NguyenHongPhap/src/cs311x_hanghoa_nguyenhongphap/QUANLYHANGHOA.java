    void xoaTable(){
        while(dtm.getRowCount()>0)
            dtm.removeRow(0);
    }
    void hienThiThongTinTable(ArrayList<HangHoa>a){
        xoaTable();
        for (HangHoa x : a) {
            if(x instanceof HangDienMay)
                dtm.addRow(new Object[]{x.getMaHang(),x.getNgayNhap(),x.getSoLuong(),x.getDonGia(),((HangDienMay) x).getNoiSanXuat(),((HangDienMay) x).tinhThanhTien()});
            else dtm.addRow(new Object[]{x.getMaHang(),x.getNgayNhap(),x.getSoLuong(),x.getDonGia(),((HangThuCong) x).getNhaSanXuat(),((HangThuCong) x).tinhThanhTien()});
        }
    }
